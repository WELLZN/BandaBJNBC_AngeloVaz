          <h1>Sobre a Banda</h1>
          Ao assumir o compromisso de passar para o público toda a alegria e musicalidade reinantes no mais autêntico carnaval baiano, a Banda Beijo na Boca fundada em 1997, torna-se a mais nova onda do axé-music no Brasil. Com todos os seus componentes esbanjando muita felicidade e alegria no que se propõe, puxado por uma das mais talentosas vocalistas que o ritmo apresenta no momento, <b>Cíntia Cris</b>, descoberta pelo diretor da banda Ângelo Vaz. Ninguém consegue ficar parado diante do palco.
          <br />
          <br />
          É com esta bagagem que a banda sobe nos palcos por onde passa esbanjando ousadia, versatilidade e pontualidade, resultado da conquista de mais fãs a cada apresentação. O Palco é sem dúvidas a casa dos músicos da Banda Beijo na Boca.
